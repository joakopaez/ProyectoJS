function addGame() {
    let name = document.getElementById("inputName").value.trim();
    let year = parseInt(document.getElementById("inputYear").value);
    let genre = document.getElementById("inputGenre").value.trim();
    let development = document.getElementById("inputDevelopment").value.trim();

    if (!name || isNaN(year) || !genre || !development) {
        Swal.fire({
            title: "Error",
            text: "Por favor, completa todos los campos correctamente.",
            icon: "error"
        });
        return;
    }

    let newGame = new Game(name, year, genre, development);
    library.push(newGame);
    localStorage.setItem("VideoGame", JSON.stringify(library))

    Swal.fire({
        title: "Juego agregado exitosamente",
        icon: "success"
    });
}



function filterLibrary() {
    let searchName = document.getElementById("inputName").value.trim().toUpperCase();
    let searchYear = document.getElementById("inputYear").value.trim();
    let searchGenre = document.getElementById("inputGenre").value
    let searchDevelopment = document.getElementById("inputDevelopment").value

    let filteredLibrary = library.filter(game => {

        let gameName = game.name.trim().toUpperCase()
        let gameYear = parseInt(game.year)
        let gameGenre = game.genre.trim()
        let gameDevelopment = game.development

        let matchesName = searchName === "" || gameName.includes(searchName);
        let matchesYear = searchYear === "" || gameYear === parseInt(searchYear);
        let matchesGenre = searchGenre === "" || gameGenre === searchGenre;
        let matchesDevelopment = searchDevelopment === "" || gameDevelopment === searchDevelopment;
        
        let result = matchesName && matchesYear && matchesGenre && matchesDevelopment;
        return result;
    });

    if (!filteredLibrary.length) {
        Swal.fire({
            icon: "error",
            title: "Juego no encontrado",
            text: "No contamos con este juego por el momento"
        });
        return;
    }

    let gameNames = filteredLibrary.map(game => game.name);
    Swal.fire({
        title: "Las coincidencias de tu búsqueda son:",
        text: gameNames.join(", "),
        icon: "success"
    });
}
